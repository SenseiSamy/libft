#include "libft.h"
#include <stdlib.h>

char	*ft_strjoin(char const *s1, char const *s2)
{
	char			*result;
	const size_t	len1 = ft_strlen(s1);
	const size_t	len2 = ft_strlen(s2);
	size_t			i;

	result = (char *)malloc((len1 + len2 + 1) * sizeof (*result));
	if (result == NULL)
		return (NULL);
	i = 0;
	while (i < len1)
	{
		result[i] = s1[i];
		i++;
	}
	while (i < len1 + len2)
	{
		result[i] = s2[i - len1];
		i++;
	}
	result[i] = '\0';
	return (result);
}
