#include <stddef.h>

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t				i;
	const unsigned char	*p1 = (const unsigned char *)s1;
	const unsigned char	*p2 = (const unsigned char *)s2;

	i = 0;
	if (!n)
		return (0);
	while (p1[i] && p2[i] && p1[i] == p2[i] && i < n - 1)
		++i;
	return (p1[i] - p2[i]);
}
